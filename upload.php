        $dbHost     = 'localhost';
        $dbUsername = 'root';
        $dbPassword = '';
        $dbName     = 'ems';
        
        //Create connection and select DB
        $db = new mysqli($dbHost, $dbUsername, $dbPassword, $dbName);
        
        // Check connection
        if($db->connect_error){
            die("Connection failed: " . $db->connect_error);
        }
        
     
        
        //Insert image content into database
        $insert = $db->query("UPDATE movies set poster='$imgContent' where Movie_id='7' ");
        if($insert){
